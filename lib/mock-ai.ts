export function generateMockArticle(params: GenerateArticleParams): string {
  const { keyword, domain, style, wordCount } = params

  // 从预定义文章中选一篇适合领域的
  const domainArticles = mockArticles[domain] || mockArticles['情感']
  const baseArticle = domainArticles[Math.floor(Math.random() * domainArticles.length)]

  // 替换关键词占位符
  let result = baseArticle.replace(/{keyword}/g, keyword)

  // 根据风格微调
  const stylePrefix: Record<string, string> = {
    '深度专业': '> 本文为深度分析文章，建议收藏后仔细阅读。\n\n',
    '轻松幽默': '> 😄 今天咱们来聊点有意思的～\n\n',
    '情感共鸣': '> 💔 每个人的心里，都有一道不愿触及的伤。\n\n',
    '干货实用': '> 📝 建议先收藏，避免以后找不到！\n\n',
  }

  result = (stylePrefix[style] || '') + result

  // 补充到目标字数
  while (result.length < wordCount) {
    result += `\n\n${generateExtraParagraph(keyword)}`
  }

  return result.substring(0, Math.min(result.length, wordCount))
}

export function generateMockTitles(params: GenerateTitleParams): string[] {
  const { keyword } = params

  const shuffled = [...titleTemplates].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, 5).map((t) => t.replace(/{keyword}/g, keyword))
}

